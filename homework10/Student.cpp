// Student.cpp

#include "Student.h"

// === Default Constructor ==================================================
Student::Student()
{
    idNum = 0;
    studentName = "";
}
// ==========================================================================

// === Overloaded Constructor ===============================================
Student::Student(int idNum, string studentName)
{
    this->idNum = idNum;
    this->studentName = studentName;
}
// ==========================================================================

// === Destructor ===========================================================
Student::~Student() {}
// ==========================================================================

// === setID method =========================================================
void Student::setID(int idNum) {

    this->idNum = idNum;
}
// ==========================================================================

// === setName method =======================================================
void Student::setName(string studentName) {
    
    this->studentName = studentName;
}
// ==========================================================================

// === getID method =========================================================
int Student::getID() {

    return idNum;
}
// ==========================================================================

// === getName ==============================================================
string Student::getName() {

    return studentName;
}
// ==========================================================================

// === displayStudent =======================================================
void Student::displayStudent() {

    cout << "ID:\t" << idNum << endl
         << "Name:\t" << studentName;
    cout << endl << endl << endl;
}
// ==========================================================================